async function runAgent(agent: Agent, store: InspectorStore): Promise<number> {
  const start = performance.now();
  console.log(`\nü§ñ [${agent.name}] ‚Äî ${agent.description}`);
  try {
    await agent.run(store);
  } catch (error) {
    console.error(`  ‚ùå Agent "${agent.name}" failed:`, error);
  }
  const elapsed = performance.now() - start;
  console.log(`  ‚è±Ô∏è  ${agent.name} completed in ${(elapsed / 1000).toFixed(1)}s`);
  return elapsed;
}

